package _18_Stacks_And_Queue.CircularQueue;

public class DynamicCircularQueue extends CircularQueue{

    // Default constructor
    public DynamicCircularQueue() {
        super();//using keyword it will call parent constructor CircularQueue(). ctrl+click on super to see.
    }

    // Constructor with custom size
    public DynamicCircularQueue(int size) {
        super(size);//it will call parent constructor CircularQueue(int size)
    }

    
}
