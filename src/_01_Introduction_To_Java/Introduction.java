// Package declaration: Packages are used to organize related classes.
package _01_Introduction_To_Java;

// Class declaration: Classes are the basic building blocks in Java.
public class Introduction {
    // main method: The entry point of the program.
    public static void main(String[] args) {
        // System.out.println(): A method to print text to the console.
        System.out.println("Hello world");

        // Comments: The lines starting with "//" are comments, providing explanations.
        // Static: A keyword indicating a method or variable belongs to the class rather than an instance.
        // void: A return type indicating the method does not return any value.
        //(String[] args) this is the command line argument.
    }
}
